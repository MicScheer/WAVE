      SUBROUTINE UTIL_FILE_COPY_80(LUNIN,FILEIN,LUNOUT,FILEOUT)

      IMPLICIT NONE

      INTEGER LUNIN,LUNOUT
      CHARACTER(80) FILEIN,FILEOUT,C80

      OPEN(UNIT=LUNOUT,FILE=FILEOUT,STATUS='NEW')
      OPEN(UNIT=LUNIN,FILE=FILEIN,STATUS='OLD')
100       READ(LUNIN,'(A80)',END=900)C80
          WRITE(LUNOUT,'(A80)')C80
          GOTO 100
900   CLOSE(LUNIN)
      CLOSE(LUNOUT)

      RETURN
      END
